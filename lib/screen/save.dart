void wordsSave(e, j) async {
  final prefs = await SharedPreferences.getInstance();
  List<String> lasetE = prefs.getStringList("missedEnglish") ?? [];
  List<String> lasetJ = prefs.getStringList("missedjapanese") ?? [];
  List<String> lasetD = prefs.getStringList("missedDate") ?? [];
  DateTime now = DateTime.now();
  lasetE.addAll([e]);
  lasetJ.addAll([j]);
  lasetD.addAll(["${now.month}:${now.day}"]);
  await prefs.setStringList('missedEnglish', lasetE);
  await prefs.setStringList('missedjapanese', lasetJ);
  await prefs.setStringList('missedDate', lasetD);
}
